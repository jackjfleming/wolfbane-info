== Tips & Tricks ==

* The more players that are executed, the more information you get. It's to your benefit to facilitate as many executions as you can. The good team's main method for killing evil players is execution as they are far less likely to die at night, so every execution is a chance to hit the Demon. If you don't, you'll at least arrive on the final day knowing a lot more about your fellow players and their roles.

* You do not learn the identity of Travellers; they are exiled, not executed. The only exception to this is the {{Traveler|Scapegoat}}, since they are explicitly executed by their character ability. However, you do not learn their alignment, only their character.

* Executing someone when you don't know their identity can lend credence to your claim as the Gravedigger. Alternatively, executing someone who has come out and is claiming a particular character allows you to confirm their story, as you will learn what character they are. Both are great ways to build trust with your fellow good team members.

* Good candidates for early executions are characters like the {{Good|Housekeeper}} and the {{Good|Librarian}}, as confirming their identity allows you to also confirm other good players. Executing the {{Good|Investigator}} or {{Good|Chef}} doesn't confirm any other players, but still does mean you can trust their powerful information and perhaps quickly execute the troublesome {{Evil|Poisoner}}.

* Executing a character like the {{Good|Seer}} may mean they don't get any more information, but you can confirm them and everything they've said up until that point. If they die during the night, they can no longer be confirmed.

* Coming out early opens you up to risks of death or poisoning. Waiting to reveal your information until a critical moment can maximise the amount of information you receive. Make sure to come out before the end of the game!

* Coming out as soon as possible is a good choice if you've learned something critical, such as the identity of the Demon. If you are shown that the executed player was the {{Evil|Alpha Wolf}}, it is very likely that a {{Evil|Successor}} just took over as the Demon!

* If an executed player is the {{Good|Village Idiot}}, you will see the {{Good|Village Idiot}} character token, not the Townsfolk they thought they were.

* If you see that a player is a good character, you can trust them, and claim what you are to them. Then, when you eventually reveal yourself as the Gravedigger, the executed players will be able to back you up.

* If you see an evil character token, it is typically unwise to be truthful with the player who was just executed. You may still want to claim to be the Gravedigger and pretend to back up their bluff, so that the {{Evil|Alpha Wolf}} and {{Evil|Poisoner}} might avoid you in future nights.

* A player who dies because of the {{Good|New in Town}} is considered executed and will react to your ability, so you will learn who they are that night.

* Beware the {{Evil|Spy}} and the {{Good|Recluse}}! They will likely register to you as good and evil characters respectively, as their abilities continue to function even when they are dead.
