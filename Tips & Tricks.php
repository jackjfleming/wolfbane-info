== Fighting the Spy ==

* Detecting the Spy is the first step in fighting the Spy. For example, the Demon kills may be a little bit too efficient. If your {{Good|Seer}} and {{Good|Hunter}} swiftly drop dead while your {{Good|Ravenkeeper}} and {{Good|Soldier}} are unscathed, that is a sign that the Demon knows more than they should.

* If you suspect a Spy, you should assume that the evil team knows everything, which means it is now in the good team's best interest to share everything they have. This will allow you to coordinate more efficiently, while also catching out evil players who didn't have their bluff totally prepared.

* Pay attention to players who spend a lot of time whispering together, particularly on the first day; while this is not always a Spy and their Demon, they often have more to talk about than the average pair of players.

* If you are a {{Good|New in Town}}, {{Good|Gravedigger}} or {{Good|Ravenkeeper}}, be wary of players who seem too eager to die and prove who they are. Because the Spy can register as good, this might be their attempt to weasel their way into your good graces.

* The Spy is particularly good at bluffing as characters like the {{Good|Housekeeper}} or the {{Good|Ravenkeeper}}, as these characters detect information that is not usually available to the evil team until those players come out. If you suspect a Spy, keep a close eye on anyone claiming to be these roles.

* Characters who detect alignment, like the {{Good|Empath}} or the {{Good|Chef}} may get information that doesn't seem to add up. This can sometimes be an indication that the Spy is causing the numbers to be off by registering as good.

* The Spy will know if they've been seen by the {{Good|Housekeeper}} or {{Good|Librarian}} as a good player, but they won't know the character. If one of these characters claims to have seen a character that neither of the people they saw are claiming to be, perhaps a Spy misregistered to them. This can be particularly significant if the {{Good|Librarian}} saw an Outsider and one of the people they saw is claiming to be a different Outsider - perhaps the Spy took a one-in-four chance, and guessed wrong.

* An {{Good|Gravedigger}} or {{Good|Ravenkeeper}} can learn that a player is in fact the Spy. This is usually a lie by the Storyteller and should regarded with suspicion.

* The {{Evil|Alpha Wolf}} is more likely to kill themselves and pass their Demonhood on if a Spy is in play due to the Spy's ability to integrate themselves into the town. Be wary of any deaths at night that do not make sense.

