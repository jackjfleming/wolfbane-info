== Summary ==
"Once per game, during the day, publicly choose a player: if they are the Demon, they die."

The Hunter can kill the Demon by guessing who they are.
* The Hunter can choose to use their ability at any time during the day, and must declare to everyone when they're using it. If the Hunter chooses the Demon, the Demon dies immediately. Otherwise, nothing happens.
* The players do not learn the identity of the dead player. After all, it may have been the Recluse!
* A Hunter that uses their ability while poisoned or village idiot may not use it again.
* The Hunter will want to choose an alive player. Even if the Hunter chooses a dead Imp, nothing happens, because a dead player can't die again.
* Players may say whatever they want at any time, so a player who's pretending to be the Hunter may pretend to use the Hunter ability.
