* Choosing living players in usually more beneficial than choosing dead players. If you learn a living player is good, you know not to execute them. If you learn a living player is evil, you know you shouldn't trust them! However, every so often, confirming that a dead player is good can be an enormous help. For example, confirming that a dead {{Good|Housekeeper}}, {{Good|Librarian}}, {{Good|Investigator}} or {{Good|Chef}} is telling the truth can help you verify their information, or picking a dead player you have suspicions about can confirm that an evil player is dead. That might even back up the {{Good|Investigator}} or {{Good|Seer}}.

