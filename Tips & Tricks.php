== How to Run ==
During the day, the Hunter can declare that they wish to use their ability. If so, the Hunter points at any player. If the chosen player is an alive Demon, declare that the chosen player dies—put a shroud on their character token in the Grimoire. If the chosen player is not an alive Demon, say "Nothing happens." Either way, the Hunter loses their ability—put the Hunter's '''NO ABILITY''' reminder token by the Hunter token.

If a player is bluffing as the Hunter and declares they wish to use their ability, act as if they were indeed the Hunter—allow time for discussion, let them make the decision, and act like you're fiddling with tokens in your Grimoire, then say "Nothing happens."

When the Hunter declares that they wish to use their ability, give the group a minute or two to discuss who the Hunter should choose. This allows the group to feel responsible for the win (or the loss!), but the Hunter always makes the final choice.
