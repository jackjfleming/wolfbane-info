== How to Run ==
If the first player to ever nominate the New in Town is a Townsfolk, immediately declare that the nominating player is executed. That player '''dies'''—put a shroud on their character token in the Grimoire. '''The New in Town loses their ability'''—put the New in Town's '''NO ABILITY''' reminder token by the New in Town token. End the nomination process and proceed to the night phase. (No one else can be executed today.)

If the first player to ever nominate the New in Town is not a Townsfolk, continue the vote as normal. '''The New in Town loses their ability'''—put the New in Town's '''NO ABILITY''' reminder token by the New in Town token.
