== Bluffing as the Housekeeper==

When bluffing as the Housekeeper, there are a few things you should keep in mind:

* You would have received your information on night one, and so should have it from that point onward. You will have been shown two players and one Townsfolk token.

* Claim to be the Housekeeper  and point to at least one evil player. Then, name the Townsfolk character (preferably one that you suspect/know is not in play). If that evil player is clever, they may realize that you are trying to make them look good, and claim to be that Townsfolk. For example, if you point to the {{Evil|Alpha Wolf}} and a random good player, and say that one of them is the {{Good|Bodyguard}}, then the {{Evil|Alpha Wolf}} may claim to be the {{Good|Bodyguard}}, which makes you both look good.

* If a good player claims to be a particular Townsfolk character, you can claim to be the {{Good|Housekeeper}}, and confirm that they are who they say they are. This can help them to trust you, and lead them astray with their own information.

* Immediately upon awakening, tell the group that a particular Townsfolk is in play. Cross your fingers and hope that you are correct! If you are, great. If not, an evil player may bluff as the Townsfolk you claimed is in play.

* The Housekeeper can be a difficult bluff, because sometimes the Townsfolk you say is in play, isn't. If this happens, you can always claim to be the {{Good|Village Idiot}} or poisoned. Another option is to claim that Housekeeper  was a bluff, and that you are actually a character that is more powerful later in the game such as a {{Good|Hunter}} or an {{Good|Gravedigger}}, and that you were trying to look like a character that has already used their ability so that the Demon would not attack you.

* If you are the {{Evil|Spy}} or have access to a {{Evil|Spy}}, they can be invaluable in providing you accurate information to back up your story.
