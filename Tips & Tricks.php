== Tips & Tricks ==

* If you're reading this having just pulled the Spy token this tip won't help you, but if you're about to play your first game, ask your Storyteller to show you an example of a grimoire, so you're prepared when they wake you up.

* Providing your memory is good, you start the game knowing everything! All characters in play and who they are will be laid out in the Grimoire. Use this information to help the evil team pick good bluffs and take out high priority targets.

* It isn't necessary to memorise everything. Some important things to remember can be:
** A character that isn't in play, for you to bluff (perhaps more than one, in case you get chance to talk to a fellow minion), and any information you might need to support your bluff.
** Some people the {{Evil|Alpha Wolf}} should kill or your {{Evil|Poisoner}} poison, like the {{Good|Hunter}} or {{Good|Gravedigger}}
** Some people the {{Evil|Alpha Wolf}} should definitely not kill, like the {{Good|Ravenkeeper}} or {{Good|Soldier}}
** Any reminder tokens you think will be important

* Take your time! The Storyteller will show you the Grimoire for as long as you need.

* In the Grimoire, you will not only see who everyone is, but the Storyteller reminder tokens. These will allow you to identify who is a {{Good|Village Idiot}}, who your {{Evil|Poisoner}} targeted, who the {{Good|Seer}} red herring is, who the Demon killed,  who the {{Good|Housekeeper}}/{{Good|Librarian}} is getting their read on, and the alignment of any Travellers. Remember that you may register as good for a  {{Good|Housekeeper}} or {{Good|Librarian}}; if you see this in the Grimoire, you can lean into that bluff early on.

* Choosing a good bluff for yourself is easy, because you know all characters that are not in play.

* You also have access to information that is hard to guess or obtain as an evil player, like player's characters. This makes you an especially potent {{Good|Housekeeper}}, {{Good|Gravedigger}} or {{Good|Ravenkeeper}}, since all of these characters learn that sort of information. You can gain the trust of good players by being able to name their character, especially if they haven't revealed yet, or if they are bluffing as something else.

* Bluffing as the {{Good|Librarian}} or the {{Good|Investigator}} and selecting powerful characters (like the {{Good|Empath}}, {{Good|Ravenkeeper}} or {{Good|Mayor}}) to mark as the {{Good|Village Idiot}} or as an evil Minion will let you undermine these players. As the Spy, you can do this before they come out, which lends credibility to your claim compared to other evil players doing the same type of bluff.

* You can provide support to your Demon and other Minions (like the {{Evil|Poisoner}}) by secretly giving them powerful characters to target early on, neutralizing them before they pose a serious threat for the evil team. In general, you are going to want to have a lot of secret conversations with fellow evil players to communicate the information that you have; prepare a solid alibi to explain why you were whispering.

* You will usually register as a good player to characters like the {{Good|Empath}}, {{Good|Chef}}, and {{Good|Gravedigger}}. Thanks to this, you will be able to gain the trust of good players much more easily than the other evil characters. This also makes you a great candidate to become the Demon in the later game. Encourage the {{Evil|Alpha Wolf}} to pass on their Demonhood to you once you are well established!

* You are the only evil player that can activate the {{Good|New in Town}} ability. Players who die this way are usually the most trusted characters in the game. Deliberately giving false information and then sacrificing yourself on a {{Good|New in Town}} can give you a platform to confuse and mislead the good team. Similarly if you know an {{Good|Gravedigger}} is in play, you can allow yourself to be executed, so when you register as good to them, they will trust you and your information. This can also be a great way to undermine an undesirable execution (for example, your Demon) - you will die and your execution will take priority over the existing vote.

* The Spy is particularly effective for bluffs that require more than one player. For example, you can claim to be the {{Good|Housekeeper}}, {{Good|Librarian}} or {{Good|Ravenkeeper}} and back up your Demon's character claim. (You should probably let your Demon know you're going to do this!)

* You will often register as good, which is great if you are sitting near an {{Good|Empath}}, or if a {{Good|Chef}} is in play. This will help you with your goal of infiltrating the good team and getting them to trust you.

* You can telegraph possible bluffs to other evil players, without needing to have a private conversation with them. Like talking about "stars" or "jewels" in order to let a partner know that you are strong in diamonds in a game of bridge, you can use subtleties of language to hint to your allies what characters they can bluff as. Or, sometimes, just tell them directly. For example, if you publicly say, "I won't say who, but somebody told me that David is the {{Good|Bodyguard}}", then David might realise what you are doing, and start bluffing as the {{Good|Bodyguard}}.

* Make sure the Storyteller knows what you are claiming to be - this will enable them to back up your bluff to other players. For example, if you are bluffing as the {{Good|Butler}} and you are executed, the Storyteller will know to show the {{Good|Gravedigger}} the {{Good|Butler}} token.

* You won't always register as good. Sometimes the town will see you as evil - and that's okay! The last thing you want is for your Demon to be executed because you're too well trusted.
