== Tips & Tricks ==

* Using your ability late in the game is far more effective than using it early in the game. When fewer people are alive, there is a greater chance that you will select the Demon and win the game. Also, if fewer than five players are alive, then the {{Evil|Successor}} cannot become the Demon after you kill it.

* However, using your ability early in the game is better than not using your ability at all because you're dead. Most players will die before the final day arrives, so the odds that you'll also be dead by then are high. Make sure you use your ability before you die. The Demon will want to kill you as soon as possible, so it is usually best to wait, then claim to be the Hunter only on the day that you intend to use your ability.

* Coordinate with the other good players to help you select which player to choose. When you tell the group that you are the Hunter, you may find that many players reveal who they are and what information they have, so that you can make the best decision about who to slay. You can make particular use of a {{Good|Seer}}'s info to find people who might be the Demon.

* Most of the time, the player that you choose to slay will not be the Demon. This is still useful to do, since you now know a player that is definitely NOT the Demon. Knowing who the Demon isn't is almost as useful as knowing who the Demon is.

* Sometimes, it can be useful to choose to slay a player, not because you think that they are the Demon, but because you just want to make sure that they are not. For example, if a player is claiming to be the {{Good|Saint}}, and is about to be executed, you can prove that they are not the Demon by using your ability on them - if they don't die, you will probably want to execute somebody else instead!

* An advanced strategy can be to try to influence the Demon's kills with your shot. If someone claims to be the {{Good|Ravenkeeper}}, try to slay them. If they aren't the {{Evil|Alpha Wolf}}, the real Demon will be forced to make a difficult choice: to kill them, and give them information, or to leave them alive late into the game with everyone knowing they aren't the Demon?

* When you choose to use your ability, the Storyteller should allow a minute or so for the group to discuss who they think you should choose. They may give you advice, but it is always your decision. This period can be a good time to pretend to be "about to choose" a particular player... just so you can watch to see how much they sweat under pressure. If, for example, you really want to slay the player claiming to be the {{Good|Mayor}}, you can tell the group that you are really keen to choose the player claiming {{Good|Soldier}}, but secretly you want to see how the {{Good|Soldier}} player reacts before making your final choice.

* If you find a {{Good|Bodyguard}} in play, convince them to protect you every night. This way, you can publicly claim to be the Hunter, safe in the knowledge that the Demon can not kill you tonight. Whilst the {{Good|Bodyguard}} is alive, you know you will always be alive tomorrow, so are under no pressure to use your ability today. If the {{Good|Bodyguard}} dies at night, you are no longer safe, but if you die at night whilst the {{Good|Bodyguard}} is still alive, then you know the player is not actually the {{Good|Bodyguard}}.

* Beware the {{Evil|Poisoner}}! If you publicly claim to be the Hunter, even if the {{Good|Bodyguard}} is protecting you, you are a prime target for poisoning.

* Your ability may work on the {{Good|Recluse}}, since the {{Good|Recluse}} might register as the Demon. If your target dies, and claims to be the {{Good|Recluse}}, then they might actually be the {{Good|Recluse}}... or they could have been the Demon, but had a {{Evil|Successor}} who is now the Demon.

* You can use your ability at any time during the day. There's no need to rush. You can even use it with the day about to end. On the block? Take your shot. Even if you don't have a clue who the Demon is, it's better to have shot someone than to die unspent.

* Remember that your ability only works on the Demon. A person surviving your shot does not prove that they are good, merely that they are not the Demon.

* Be aware that the {{Evil|Alpha Wolf}} may change players throughout the game. If you choose to slay a Minion early in the game, nothing will happen, but that same player may be the {{Evil|Alpha Wolf}} by the end of the game.
