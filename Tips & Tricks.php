== Bluffing as the New in Town ==

When bluffing as the New in Town, there are a few things you should keep in mind:

* You would never wake during the night. Pay attention to who is the first person to nominate you; it's only the first nomination that counts.

* A good player will never be executed due to nominating you. This means that when you are nominated, one of you will look evil, or at least village idiot. If you can, get a player that the group is suspicious might be evil to nominate you. This way, if they are believed to be evil, you will be believed to be good.

* If a New in Town is nominated, but either player is the {{Good|Village Idiot}}, the New in Town ability does not trigger, since either the New in Town is the {{Good|Village Idiot}} (not the New in Town), or the nominating player is an Outsider (not a Townsfolk). When nobody dies after nominating you, you can usually make a strong case that one of you is the {{Good|Village Idiot}}. This has the added benefit of any real {{Good|Village Idiot}} gong undetected by the group, or an outsider, perhaps even a {{Good|Saint}}, being executed because the good team think there are too many Outsiders in play.

* If you are a Minion, you can get your Demon to nominate you and then bring suspicion on yourself. The more evil you look, the less evil the Demon looks, since your Demon will be the 'victim' of your bluff, and players will be more inclined to trust them.

* Beware bluffing as the New in Town if a real New in Town is in play. If you have told the group that you are the New in Town, and the real New in Town proves themself by getting a Townsfolk executed, you can not possibly be the New in Town. You will need to get an evil character to nominate the real New in Town so that this doesn't happen, so the good team remains confused about who the real New in Town is.

* Bluffing as the New in Town, being nominated, then continuing to live afterwards, is a great opportunity to come up with another bluff. Claiming that you are actually the {{Good|Gravedigger}}, or the {{Good|Bodyguard}} for example, can be believable even if you claimed to be the New in Town for the first day or two. Since the Demon will usually rather not kill a New in Town that is under suspicion of being evil, and instead want to kill an {{Good|Gravedigger}}, or a {{Good|Bodyguard}}, then 'lying about who you were temporarily' can be very believable.

* If you are confident that the group believes you are the New in Town, then insist that the player who is the most dangerous to you be the one to nominate you! They will look evil when you don't die. For example, if an {{Good|Empath}} is giving the evil team a lot of trouble, you can insist that they nominate you, which will discredit them.

* Bluffing as the New in Town can be particularly potent if you are a Minion, since a good team who believes that either you or the person who nominated you is evil might spend two days executing both of you and not executing your Demon.
