== Tips & Tricks ==

* The Successor is at her strongest when you co-ordinate with the Demon and figure out a strategy that you both like, since the Demon can safely die. Maybe you accuse the Demon of being evil, and the Demon does everything possible to make the good team think they are indeed evil? This makes you look very good, and trustworthy. Maybe the Demon kills themselves of a night time and claims to be the {{Good|Ravenkeeper}}, saying that you are the character you say you are. Maybe the Demon accuses you of being evil, but does so unconvincingly, so that you look good by comparison. Maybe you and the Demon both claim to be the same character, such as the {{Good|Mayor}}? After all, "one of you must be the {{Good|Mayor}}", right? It doesn't matter which one of you dies, since the game continues with a living Demon either way.

* In the early game, staying alive is more important for you compared to other Minions. Whist it doesn't really matter if the {{Evil|Baron}}, or {{Evil|Poisoner}} dies early (since they can often cause a lot of disruption early in the game), you will need to survive until the end of the game if you intend to become the Demon at some point. Waiting, listening, then claiming to be a character that you know is not in play often achieves this best.

* Timing is important. Remember that if fewer than five players are alive when the Demon dies, the game is over and good wins. If your Demon has a lot of heat on them, and you think that they won't survive to the final day, take action and get your Demon killed while five or more players are still alive... before it's too late.

* Whilst you are the Successor, you will not register as the Demon. So, if you can get yourself confirmed as such by good characters, such as the {{Good|Hunter}} or the {{Good|Seer}}, then become the Demon at a later stage, then the good team will be very confused about the identity of the real Demon, as their information won't add up.

* If the Demon is playing well, and seems to be trusted by the good team, there is no need to use your ability. You can play the normal Minion role of spreading false information, breeding distrust among the good players and generally being troublesome whilst ignoring your character ability.
