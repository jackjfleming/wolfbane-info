== Summary ==
"The 1st time you are nominated, if the nominator is a Townsfolk, they are executed immediately."

The New in Town may inadvertently execute their accuser, confirming which players are Townsfolk in the process.
* If a Townsfolk nominates the New in Town, then that Townsfolk is executed immediately. Because there can only be one execution per day, the nomination process immediately ends, even if a player was about to die.
* Only Townsfolk are executed due to the New in Town's ability. If an Outsider, Minion, or Demon nominates the New in Town, nothing happens, and voting continues.
* The New in Town's ability is powerful because if a Townsfolk nominates them and dies, then both characters are almost certainly Townsfolk.
* After being nominated for the first time, the New in Town loses their ability, even if the nominator did not die, and even if the New in Town was poisoned or village idiot.
