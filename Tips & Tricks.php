== Bluffing as the Bodyguard==

When bluffing as the Bodyguard, there are a few things you should keep in mind:

* The Storyteller would have woken you on nights that the {{Evil|Alpha Wolf}} wakes - every night except the first - and you would have pointed to a player.

* Your choice for who you 'protect' should have some logic behind it. If a publicly revealed {{Good|Seer}}  dies on your watch, that can be a big clue that you are not who you say you are. However, if you tell the group that you don't believe that the {{Good|Seer}}  is telling the truth, and that you instead protected a player that has privately revealed that they are the {{Good|Empath}} to you, your choices seem much more reasonable. You can come up with all sorts of reasons about why you protected player A instead of player B, particularly if you prepare what you are going to say beforehand.

* The Demon can choose to pick a dead player instead of an alive player at night. If you are claiming to be the Bodyguard, getting the Demon to do this supports your bluff. When nobody dies at night, all players will want to know why. This also means that the player you claimed to have protected from the Demon will look more trustworthy, since the Demon almost always attacks good players. The player that you claim to protect is likely to be seen by other players as the same alignment as you, so you can either ingratiate yourself by claiming to have picked a good player, or back up the Demon by saying you picked them.

* The Bodyguard usually operates in secret, not revealing who they are to the group until they have successfully protected a player from death. You can use this secrecy as an excuse to talk to many players in private, either spreading lies to the good players, or coordinating with your evil team mates. If anybody asks why you are being so secretive, the players that believe you are the Bodyguard will come to your defense.

* If you are still alive on the final day, and a player has died each night this game, the good players will probably be convinced that you are either a particularly inept Bodyguard, or just evil. If you are a Minion, this can be quite helpful, since the good players will want to execute you, ending the game with an evil victory.

* You can draw attention to yourself as a Minion by claiming to have protected the player who died last night. There are legitimate reasons why it might have happened - you could be the {{Good|Village Idiot}} or poisoned, but providing such an outlandish topic of discussion distracts from the information that the {{Good|Seer}} has pointing directly at your Demon.
