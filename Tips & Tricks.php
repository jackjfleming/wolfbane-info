== Tips & Tricks ==

* The Village Idiot will never know that they are the Village Idiot - they will start the game with a Townsfolk token and behave as that Townsfolk normally while receiving bad information. Figuring out if you are the Village Idiot can only be discovered via gameplay; be looking for tells like information you receive being demonstrably false, or your ability failing (e.g. A {{Good|Soldier}} dying at night).

* Figuring out if a Village Idiot is in play is important - this will allow Townsfolk to know whether or not they should be scrutinizing their information. The most obvious tell that a Village Idiot may be in the town is if there are more or fewer Outsiders than you expected (if more, there might be a {{Evil|Baron}} who has let a Village Idiot into town). 

* If you suspect a Village Idiot is in play, it is important to determine which of the Townsfolk it is as soon as possible. There can only be one Village Idiot in play, so knowing who it is will allow you to treat their information skeptically, but it will also mean that all other Townsfolk can probably trust what they know to be true and reliable.

* Differentiating the effects of a Village Idiot from a {{Evil|Poisoner}} can be difficult, but crucial. If only one person seems to be receiving false information, they might be the Village Idiot. If multiple people are receiving sporadically good and bad information, that's probably the work of a dastardly {{Evil|Poisoner}}. Try to extrapolate from the case where either one is true, and see what inferences you can draw from that. Perhaps a Village Idiot being in play means that the {{Good|Butler}} is lying? Or if there's a {{Evil|Poisoner}} in a game with only one Minion, you know that nobody who's been executed could have been the {{Evil|Alpha Wolf}}.

* Characters like the {{Good|Gravedigger}} and {{Good|Ravenkeeper}} will see the Village Idiot character when checking that player. You do not register as a Townsfolk - you just think you are! This is also true for characters like the {{Good|New in Town}} - a Village Idiot cannot activate them because they are not really a Townsfolk, they're an Outsider.

* If you have figured out you are the Village Idiot, try to reverse engineer what you know. For example, if you thought you were the {{Good|Empath}} and have been getting a read of '0', you should be suspicious that you have at least one evil neighbour after all. Or, if you thought you were an {{Good|Investigator}}, you can probably assume that neither of the two players you saw were Minions!

* Remember that the Village Idiot receives ''unreliable'' information, not false information. In the majority of cases, the information you receive will be wrong, but sometimes the Storyteller may tell you something that is true. This can happen when the alternative would definitely reveal you are the Village Idiot (e.g. The Village Idiot {{Good|Ravenkeeper}} choosing themselves will be shown the {{Good|Ravenkeeper}}).

* Keep in mind that characters like the {{Good|Hunter}}, {{Good|Bodyguard}} and {{Good|Mayor}} cannot impact the game with their ability when they are actually the Village Idiot. For example, a {{Good|Hunter}} will not kill a good player instead of the Demon; their shot will always fail.
