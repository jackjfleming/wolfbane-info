== Tips & Tricks ==

* Identifying targets for poison is vital. Characters who get information over several nights like the {{Good|Seer}} or {{Good|Gravedigger}} are great choices if you can find them. You can also make your choice based on the player; players who are strong leaders and rallying the good team or players who are quiet and trying not to attract attention are good options.

* Undermine players who trust each other by poisoning the characters getting key info in that clique. If a group is rallying around an {{Good|Gravedigger}}, you can turn the entire group on someone innocent by poisoning them at the right time.

* Co-ordinate with your Demon so that you are not selecting the same players - unless they are a character like the {{Good|Ravenkeeper}}, you'll just be wasting your poison for the day!

* Targeting characters like the {{Good|New in Town}}, {{Good|Hunter}} or {{Good|Mayor}} can be excellent, but only if they use their ability while they are poisoned. You'll need to convince them to use their ability, or time it right so that they are compromised at the moment they choose to activate.

* It's risky, but you can poison your own Demon, and use the lack of death for that night to bluff as the {{Good|Bodyguard}} or {{Good|Soldier}}. (You should probably warn your Demon you're going to do this!)

* If everyone is on board with the idea, try poisoning the {{Evil|Successor}}. If the {{Evil|Alpha Wolf}} chooses to kill themselves, the {{Evil|Successor}} will become the new Demon... unless you poison them first! That way, you or a third Minion can become the new {{Evil|Alpha Wolf}} and keep the {{Evil|Successor}} in play. Just don't get your {{Evil|Alpha Wolf}} killed while the {{Evil|Successor}} is still poisoned!

* You will usually be more efficient as a Poisoner by selecting multiple targets around the town rather than focusing on one player for the entire game. You only really need to poison the {{Good|Seer}} on the night they're checking your Demon, for example - then you're free to mess with other characters. This method of poisoning is also more difficult to detect than sticking to one player.

* On the other hand, if you only poison one player, that player might end up looking like the {{Good|Village Idiot}}, perhaps hiding the real {{Good|Village Idiot}}, or making it look like a {{Evil|Baron}} is in play.

* On the first night, if you don't know who to poison, poison a player sitting next to the Demon. You know which player is the Demon, and on the off chance that an {{Good|Empath}} is sitting next to the Demon, you'll be giving some very damaging information to the good team. 

* Make use of your {{Evil|Spy}}! They know who the best targets are and can guide you in the right direction.
