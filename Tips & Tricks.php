== Fighting the Baron ==

* If the number of Outsiders is different to what it should be by default (e.g. You have three Outsiders but were expecting one), then you probably have a Baron. At this point, you have several options:
** If you believe all of the Outsider claims, keep them alive! These are basically confirmed good players even if they don't have fantastic abilities.
** If you are not certain there is a Baron, then it probably best to take out the Outsiders quickly; it could actually be evil players bluffing to create the appearance of extra outsiders to avoid suspicion.
** If you believe the Outsiders, but the count isn't quite right, then one of the Townsfolk is probably a {{Good|Village Idiot}}, and you should be trying to figure out who they are as soon as possible.

* The {{Good|Librarian}}, {{Good|Investigator}}, {{Good|Ravenkeeper}} and {{Good|Gravedigger}} are all characters that greatly help you deduce the presence of a Baron.

* Unlike other Minions, the effect the Baron has on the game is pretty obvious; you will soon notice if there are Outsiders when there shouldn't be, or more of them when expected. Determining that there is a Baron is actually great news, since it means (especially in smaller games) that there is less likely to be a {{Evil|Poisoner}}, {{Evil|Spy}} or {{Evil|Successor}}.

* Because they don't benefit as much from staying alive as much as other Minions, Barons will often tend to be bolder and more aggressive with their bluffs or plays. A Baron does not fear death; they will deliberately double up with another player, try to get the first nomination of a {{Good|New in Town}}, antagonize a {{Good|Hunter}}, or otherwise just try to sow chaos. If you think a player might be having a little bit ''too'' much fun with the chaos they seem to be creating, they may be a Baron!

* Unlike the other Minions, the Baron's effect on the game happens before anything else and cannot be changed. What this means is that unlike other evil players, it isn't completely necessary to execute them. Taking out an evil vote can be advantageous, but if you have a better option (e.g. A lead on the {{Evil|Alpha Wolf}} or {{Evil|Poisoner}}), then take care of that instead.

* Don't forget that the {{Evil|Alpha Wolf}} can pass on their Demonhood to a Baron! Ignoring the Baron until it is too late might put you in a difficult position of trying to figure out if they have become the Demon on the final day.

