== Summary ==
"You do not know you are the Village Idiot. You think you are a Townsfolk character, but you are not."

The Village Idiot player thinks that they are a Townsfolk, and has no idea that they are actually the Village Idiot.
* During setup, the Village Idiot's token does not go in the bag. Instead, a Townsfolk character token goes in the bag, and the player who draws that token is secretly the Village Idiot for the whole game. The Storyteller knows. The player does not.
* The Village Idiot has no ability. Whenever their Townsfolk ability would affect the game in some way, it doesn't. However, the Storyteller pretends that the player is the Townsfolk they think they are. If that character would wake at night, the Village Idiot wakes to act as if they are that Townsfolk. If that Townsfolk would gain information, the Storyteller may give them false information instead—and the Storyteller is encouraged to do so.
		</div>

		<div class="small-12 large-6 columns" style="padding-right: 0;">
== How to Run ==
While setting up the game, before putting character tokens in the bag, remove the Village Idiot token and add a Townsfolk character token. Add the Village Idiot's '''IS THE VILLAGE IDIOT''' reminder token to the Grimoire. Put the swapped Townsfolk character token in the bag, not the Village Idiot character token.

While preparing the first night, put the Village Idiot's '''IS THE VILLAGE IDIOT''' reminder token by any Townsfolk character token, changing that player's character to the Village Idiot. They are now an Outsider, and do not have the ability of this Townsfolk character. (But they think they do.)

During the game, act as if the Village Idiot is actually this Townsfolk character. (If that character would wake to act at night, the Village Idiot wakes to act. If their ability would give them information, you can give false information to them if you wish. See "Village Idiotenness and Poisoning" in the rulebook.)
