== Bluffing as the Gravedigger==

When bluffing as the Gravedigger, there are a few things you should keep in mind:

* Each night, apart from the first night, the Storyteller would have woken you up and shown you the character token of the player who was executed yesterday.

* A good Gravedigger may very well wait a few days before revealing that they are the Gravedigger. Feel free to stay quiet for a while, or even claim to be a different character at first, to seem like an Gravedigger bluffing as something else.

* You can make yourself look good by confirming that good players are who they say they are. For this strategy to be successful you will need to find out who they are beforehand. If you are the {{Evil|Spy}}, or have access to a {{Evil|Spy}}, then you can be more convincing in this due to your complete knowledge of everybody's characters.

* You can make key players look evil by claiming that they are Minions, or even the Demon, if they are executed. This is risky, as the player in question will know that you are probably evil, but potentially game winning, as the rest of the group won't know that you are lying. Since most of the time more good players will be getting executed than evil players, use this technique sparingly. If you find that you aren't believed, you can always claim to be the {{Good|Village Idiot}} or poisoned.

* If another Evil player dies by execution, you can safely claim that they are either a good or evil character. If you claim that they are a good character, both of you confirm each other's stories, so you both look more trustworthy. If you claim that they are an evil character, they should (hopefully) act a little more evil, and publicly subtly imply that they are, in fact, evil, making you appear to be the real Gravedigger.

* A good way to undermine an executed Townsfolk character is to claim you saw the {{Good|Village Idiot}} token for them instead. If the group believes you (and especially if the executed player believes you), then the group will assume that any information they received was unreliable. If you don't know which character a player is, claiming that they are the {{Good|Village Idiot}} is a good way to hide this fact.
