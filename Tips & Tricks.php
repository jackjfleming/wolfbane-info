== Tips & Tricks ==

* If the Demon attacks you, nobody dies this night. This has two benefits. Firstly, since no good player has died at night, there are more players alive than there ought to have been, allowing the good team more time to execute. Secondly, no death of a night time supports your claim that you are indeed the Soldier.

* You will never know for sure that your ability has worked. If you wake up to no deaths, perhaps there is a {{Good|Bodyguard}} in play, or the {{Evil|Alpha Wolf}} decided to take a night off terrorising the living and attack someone's already dead corpse. Both of those look identical to the Demon attacking you in the night.

* Consider bluffing as a juicy Demon target, such as the {{Good|Seer}}, {{Good|Gravedigger}}, {{Good|Empath}} or {{Good|Hunter}}, to attract a Demon attack.

* If you can prove that you are the Soldier, then the good team has an enormous advantage on the final day. Instead of good choosing between three (or four) players to execute, they will be choosing between two (or three). Getting an  {{Good|Empath}} or {{Good|Seer}} to confirm you as not the Demon can be very helpful.

* A clever Demon will not attack you again after the first time unless they think they can kill you. If you can convince the Demon that a {{Good|Bodyguard}} protected you, the Demon might try to attack you again.

* If there are no deaths at night, don't immediately claim to be the Soldier! Perhaps the Demon attacked another player protected by the {{Good|Bodyguard}} and has no idea you were ever protected.

* If you are unsure whether they are good or evil, allow powerful information characters to bluff as your role. This way, the Demon is more likely to attack you, and less likely to attack them. You can even talk with these players secretly, and agree to publicly claim to be each others' characters - they stay safe, whilst you reveal their information to the group..

* If you die at night, you were either poisoned by the {{Evil|Poisoner}} , or you are the {{Good|Village Idiot}}. This is big information, and very helpful for the group to know. If you were poisoned, then a {{Evil|Poisoner}} is still alive (and in a one Minion game, you now know which Minion is in play!), which also gives you information about the alignment of the dead players. If you are the {{Good|Village Idiot}}, then nobody else can be the {{Good|Village Idiot}}. Either way, you learn something useful.

* Tell the group you are the Soldier at some stage during the game. After all, good will need as much information as they can get at their disposal.

* If you have told nobody that you are the Soldier, you are still alive on the final day, and a death has occurred every night so far, then it is probable that a {{Evil|Spy}} is in play and telling the Demon who to attack. If you notice this happening, pay attention to who is whispering to who.

* If you never tell anyone who you are, your role can combo in a very risky way with the {{Good|Mayor}}. You might be more confident to execute nobody in final three than anyone else in your position. Even if the {{Evil|Alpha Wolf}} is pretending to be the {{Good|Mayor}}, and you execute no one, evil hasn't won yet! The Demon still has to kill either you or the other surviving player, and just maybe they'll guess wrong, and you will wake to a second day with just three players alive.
