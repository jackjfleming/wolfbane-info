== Bluffing as the Investigator==

When bluffing as the Investigator, there are a few things you should keep in mind:

* You would have received your information on night one, and so should have it from that point onward. You will have been shown two players and one Minion token.

* Pick two Good players, and claim that one of them is a Minion to try to get get those players executed. Choosing the {{Evil|Poisoner}} or {{Evil|Successor}} is usually best if you want the good team to be motivated to execute them both. Good players may not execute a suspected {{Evil|Baron}} or a {{Evil|Spy}} immediately, but they might. 

* If you are the Demon, or even an ambitious Minion, telling the good team an actual Minion can make you look extremely trustworthy, particularly if other good players like the {{Good|Gravedigger}} support your information. This can be greatly enhanced if the Minion does everything they can to look as evil as possible. After all, the more like a Minion they appear to be, the more like the Investigator you appear to be.

* If you falsely claim that a {{Evil|Baron}} is in play, another evil player will need to claim to be an Outsider at some point. Otherwise, good will eventually question why there is not the appropriate number of Outsiders in play for a {{Evil|Baron}} and probably execute you. If you can talk privately to an evil player to give them this hint, great. If another evil player claims to be the Investigator and that a {{Evil|Baron}} is in play, you may want to claim to be an Outsider in order to back up their story.

* Communicating your information secretly to one or two good players can sow an enormous amount of distrust. They will often tear themselves apart without you having to do much more.

* Claiming to have seen a Minion that is not actually in play can cause much confusion, or back up other evil players' bluffs. For example, bluffing that a {{Evil|Baron}} is in play will help confirm a Demon who is bluffing as an Outsider. Bluffing that a {{Evil|Poisoner}} is in play when there is actually a {{Evil|Successor}}, can make good distrust their own information.

