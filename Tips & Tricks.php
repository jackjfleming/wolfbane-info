== Tips & Tricks ==

* There are many strategies for evil players, particularly the Imp, that are covered in more detail in [[Player Strategy]] section. Generally speaking, as the Demon, you want to look trustworthy, spread false information where possible, get your Minions to confirm that you are a good player, and sacrifice them when needed. Take a look at this section for more information.

* Stay alive during the day at all costs. Even in games with three Minions, do not assume there is a {{Evil|Successor}} in play. (However, if you talk to your Minion(s) and you learn that there is, then ignore this advice!)

* If you are under suspicion, night time suicide is always an option. Killing yourself and passing the Demonhood to a Minion that is looking more trustworthy robs you of the support of their continued voting power, but survival is more important. You can also do this when there is no suspicion on you - people tend to trust players that die at night. Either way, if you die at night, giving false information to the good team the day after, such as claiming to be the {{Good|Ravenkeeper}}, can change the course of the game.

* Your Minions are pawns to be sacrificed! Kill them! Kill them all! You're the only one that matters!

* At the beginning of the game, the Storyteller will give you three possible bluffs to choose from. You can bluff as whatever you want, of course, but these few bluffs are safe. Since the Imp can die at night, and pass the Demonhood to a Minion, both the more passive and the more aggressive bluffs can be useful. If you are new to the game, or don't quite understand how certain characters work, either ask the Storyteller for help, or bluff as a more passive character, such as the {{Good|Soldier}} or the {{Good|Recluse}}. If you are more adventurous, more high-risk high-reward bluffs such as the {{Good|Seer}} or {{Good|Gravedigger}} can be risked.

* A very bold strategy, to be employed on occasion when playing with players who are more familiar with the game, is to claim to be a character that you did not see as one of your bluffs. If you're bluffing as a character that's really in the game, then maybe the good team will assume that you're just a {{Evil|Baron}} who didn't learn any out of play characters, and kill the other players instead. Of course, they might decide that you're actually the {{Evil|Successor}} and execute you just to be on the safe side.

* When choosing who to kill, you generally want to kill powerful, troublesome, information-gathering Townsfolk sooner rather than later, such as the {{Good|Empath}}, {{Good|Gravedigger}}, {{Good|Bodyguard}} or {{Good|Hunter}}. Removing their information (or power) from the game early can help you later on. Every so often, deliberately keeping one of these Townsfolk alive until the final day can double-bluff the good players. They may ask this player "If you are really the {{Good|Seer}}, why are you still alive?", and kill them.

* Also, when deciding who to attack at night, you are best killing the more trusted players earlier rather than later. A {{Good|New in Town}} that everybody knows is the {{Good|New in Town}} will never be executed, which decreases your odds of being executed later on. A pair of Outsiders that everybody believes are actually Outsiders will need to disappear, because they will not be executed either. On the final day, you will want at least one other player, preferably two, to be looking more suspicious than you.

* Send your Minions out on information-gathering missions. Gather as much information as you can about who is who. This will help you avoid attacking troublesome characters like the {{Good|Ravenkeeper}} or the {{Good|Soldier}}, and the problems that consequently follow.

* Talk to your Minions! Co-ordinate with them, by helping them bluff. Agree that one of you will look as evil as possible, while the other one will take the Demonhood and survive to the end of the game. Find out what your {{Evil|Spy}} knows. Co-ordinate with your {{Evil|Poisoner}} so that you choose different players at night. Let your {{Evil|Baron}} run wild. Learn if your {{Evil|Successor}}'s plan is to kill you, or keep you alive.

* There are lots of reasons why you should register as an evil Alpha Wolf to good players. If this happens, don't despair! Maybe you can convince the group that {{Good|Empath}} is village idiot? Maybe you can argue that you are that {{Good|Seer}}'s "red herring"?

* If you don't want to kill one night, you can choose a dead player instead. This can also help you bluff well as the {{Good|Soldier}} or the {{Good|Bodyguard}}, as no death of a night-time makes it look like your {{Good|Soldier}} or {{Good|Bodyguard}} ability triggered. If no one is dead after the first day, there are no dead players to 'sink' a kill into, but if you're committed to bluffing as one of those characters, just ask your {{Evil|Poisoner}} pick you!
