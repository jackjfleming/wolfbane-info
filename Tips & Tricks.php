== Fighting the Successor ==

* When the Successor is in play, the Demon can get away with doing certain things they would not be able to otherwise. For example, they can be successfully executed, or die by a {{Good|Hunter}}'s ability and the game will continue. If you are certain that you definitely took out the {{Evil|Alpha Wolf}} but the game is still going, you should be suspicious that a Successor is in play. It is possible for an {{Good|Gravedigger}} to correctly see the {{Evil|Alpha Wolf}} token without being the {{Good|Village Idiot}}!

* If the {{Evil|Alpha Wolf}} kills themselves at night, the Demonhood must always pass to the Successor first before any other Minions. This can allow you to narrow down where the new Demon has ended up if the {{Evil|Alpha Wolf}} gets away from you.

* The Demon and the Successor may co-ordinate on their bluffs to confirm each other. For example, the Successor may claim to be the {{Good|Housekeeper}} and "confirm" the Demon's claim. This has the benefit of making them both look good, and potentially set up passing on the Demonhood later in the game.

* The Successor does not register as a Demon, which means characters who detect Demons specifically will appear to get a clean read on them. For example, the {{Good|Seer}} will not detect them, and the {{Good|Hunter}} ability will not work on them. Be wary of players who are overeager to be checked or confirmed, especially early in the game.

* If you know a Successor is in play (e.g. Getting a read from an {{Good|Investigator}} or a {{Good|Ravenkeeper}}), then a good strategy can be to leave them alive. On the final days when three players are left alive, you no longer have to answer the question "who is the Demon", but "did the Demon pass to the Successor". If you think they did, you execute the Successor. If you don't think they did, you eliminate the Successor from contention and now have a one in two shot at the Demon overall.

* Unlike other Minions, the Successor can be bolder about targeting their own Demon, and may even deliberately try to get them killed to make themselves look better. If you suspect someone is a Successor, pay close attention to who they are targeting, and if their allegiances abruptly change; if they have been targeting a particular player and then change their tune once it gets below five players, then that may very well be their Demon.

* The Successor is usually a backup plan, not a phase two; if the Demon is well integrated into the town or not under much suspicion, the Successor may end up behaving more like a regular Minion, and sacrifice themselves more freely. If you discover a Successor throwing themselves on their sword, it can be worthwhile to be suspicious of players who have been considered trustworthy up until that point.

