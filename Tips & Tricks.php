== Bluffing as the Village Idiot ==

When bluffing as the Village Idiot, there are a few things you should keep in mind:

* You would not know that you are the Village Idiot at first. You would receive a Townsfolk character. The Storyteller would treat you as if you were that Townsfolk; check the relevant 'bluffing as' section to see how that character would wake (or not!)

* The Village Idiot is the only good character that you can claim to be without another good character calling you out for claiming the same character as them.

* Bluff as a character that gains information more passively, such as the {{Good|Seer}}, {{Good|Investigator}}, {{Good|Empath}}, {{Good|Housekeeper}}, {{Good|Gravedigger}} or {{Good|Librarian}} and give deliberately and obviously false information. If your information is ridiculous (like claiming one of two players is the {{Good|Saint}} whilst neither is claiming to be the {{Good|Saint}}), you look obviously the Village Idiot.

* Bluff as a character that has a power that can fail, then when your fake ability fails, act surprised, and put forward the possibility that you are the Village Idiot. For example, bluff as the {{Good|Bodyguard}}, and claim the player you protected died anyway. Bluff as the {{Good|Soldier}} and kill yourself of a night time, or get the Demon to kill you. Bluff as the {{Good|Hunter}} or nominate the {{Good|New in Town}}, and act surprised when these character abilities have no visible effect.

* Insinuate a Village Idiot is in play, and lead the group on a hunt to find out who it is. This will distract them from the main goal of finding the Demon, and will make them distrust their own good team.

* Insinuate that a particular player is village idiot. This is especially helpful if they have a damning case against your team, such as a {{Good|Gravedigger}} claiming that your dead {{Evil|Poisoner}} is indeed the dead {{Evil|Poisoner}}.

* Convincing the group that a Village Idiot is in play, or that a particular player is the Village Idiot, is MUCH easier to achieve if the number of people publicly claiming to be Outsiders supports your story. For example, if you are saying that the {{Good|New in Town}} is village idiot, and there is supposed to be just the one Outsider in play, but the real {{Good|Saint}} has revealed who they are, then the only way for the {{Good|New in Town}} to be the Village Idiot is if the {{Good|Saint}} is lying. If you bluff as an Outsider, or if a fellow evil player bluffs as an Outsider, then it is much easier to convince the good team that a Village Idiot is in play, because the extra Outsiders make it look like a {{Evil|Baron}} is in the game, which would add two extra Outsiders.

* If you want a fellow evil player to back up your bluff as the Village Idiot, get them to claim to be the {{Good|Librarian}}. The {{Good|Ravenkeeper}} and {{Good|Gravedigger}} work well too, but one of you will have to die for that to work.

* If you intend to bluff as the Village Idiot later in the game, and you believe you can succeed, initially give opposite information to what you want the good team to believe. If you are claiming to be the {{Good|Empath}} for example, and later in the game you want to execute your good neighbours, then initially tell the group that both your neighbours are good - if later on, the group comes to believe that you are the Village Idiot, then they will assume that at least one of your neighbours is evil and execute them both.

