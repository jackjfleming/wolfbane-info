== Tips & Tricks ==

* The Housekeeper is deceptively powerful. Even though you don't gain information on the evil players, you can confirm the identity of a good player. This player should be your focus for the game, because if they have great information, then you have that information too. If they have a useful ability that they choose to use, you can help them use it wisely.

* When the Housekeeper  is poisoned or is actually the {{Good|Village Idiot}}, they will often get information that is easy to figure out is incorrect—at least compared to other characters. While the {{Good|Chef}} has no clue as to whether their "1" is incorrect, if both of the players the Storyteller pointed at tell you they're a different character to the one the Storyteller showed you, it is likely that you are the {{Good|Village Idiot}} or poisoned. You can use this information to your team's advantage: if you know you are the {{Good|Village Idiot}}, you know nobody else is; if you think you were poisoned on the first night, nobody else could have been. Furthermore, if your information seems like it's correct, it probably is, because of how easy it is to tell when it's wrong.

* You know that of the two players you are shown, one must be the Townsfolk you are shown. Importantly, this means that you know that the person you see is not the {{Good|Village Idiot}}.

* To find out which of the two players is the Townsfolk, either ask the group publicly or have a private conversation with each player individually. It is usually best to reveal what you know before the Townsfolk in question says who they are so they trust you more.

* If you share your information on the first day, and speak up quickly, the good team has some solid information to begin with. This is particularly useful if you confirm another information-receiving Townsfolk like the {{Good|Empath}} or the {{Good|Seer}}.

* Waiting until the final day - or at least very late in the game - to share your information with the group can also be very useful. If you can keep the Townsfolk that you know alive until the final day, then you know one player who is not the {{Evil|Alpha Wolf}}! This can either make the Demon player obvious to you, or at least reduce the possible Demon players down to 1 in 2 instead of 1 in 3.

* Talk to the Townsfolk player that you know, and secretly let them know that you know who they are. This way, you can hopefully form an alliance, and can come to each other's defence if either of you are nominated for execution, whilst avoiding telling the group (and therefore the evil team) who the other is. This is particularly useful if you learn a character that the Demon really wants to attack, like the {{Good|Seer}}, or even a character that the Demon wants to avoid attacking at all costs, like the {{Good|Ravenkeeper}} or the {{Good|Soldier}}.

* After talking to the Townsfolk that you know in private, and confirming who they are, you can tell the group that they are a different character than they actually are. This strategy is useful if you want to protect a powerful Townsfolk from characters like the {{Evil|Poisoner}}, or to trick evil players into attacking a Townsfolk that has already used their ability, such as the {{Good|Chef}}.

* You can claim to be a more powerful character than you actually are. You start with all the information you're going to get, so if the demon kills you, they aren't killing the {{Good|Hunter}} or the {{Good|Seer}}. You may also want to consider nominating a {{Good|New in Town}}, and confirming yourself, the {{Good|New in Town}}, and the Townsfolk you were shown all to be good.

* Sometimes the Storyteller will point to evil players as possible Townsfolk to you. If you think this might be the case, don't say directly which Townsfolk you know to be in play. You might instead state a false Townsfolk character to try and trick evil players into admitting to being a character that you didn't learn, or you can tempt good players into revealing their Townsfolk character to you before you reveal what you know to them. This allows you to trust them more... but they may trust you less!

* If the 'wrong' player the Storyteller pointed to is an evil player, telling them the Townsfolk you saw may alert the {{Evil|Alpha Wolf}} to the presence of the character you saw. If you saw the {{Good|Empath}}, for example, telling the Demon that may cause the {{Good|Empath}} to be killed in the night.

* The two of you can reveal your characters publicly to the group, without having a private conversation beforehand. This goes a long way towards proving publicly that you are both telling the truth.

* Publicly reveal which character is in play, but not which player it is. This way, the evil team gets little information, but the Townsfolk in question will trust you, and will look more trustworthy when they reveal who they are to the group.

* You could claim to be the Housekeeper and point to a player you suspect is evil, stating that they are the Townsfolk you learned about. An opportunistic evil player may pounce on an opportunity to appear good, and claim that they are, in fact, the Townsfolk you learned about, thus outing them to you as, at the very least, a liar.

* Beware of the {{Evil|Spy}}! They may register as a Townsfolk character to you. That player who you think is the {{Good|Investigator}} may not be the {{Good|Investigator}} after all...

* Remember that while the {{Evil|Spy}} is likely to know that you've seen them as a Townsfolk, they won't know which Townsfolk you've seen them as.
