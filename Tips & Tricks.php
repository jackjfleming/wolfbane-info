== Bluffing as the Seer==

When bluffing as the Seer, there are a few things you should keep in mind:

* You wake every night, including the first night, and should have information for each night you are alive. You would have pointed at two players and the Storyteller would have either nodded or shaken their head. When you reveal that you are the Seer, the good team will expect you to have a detailed explanation of every night's activities. Be prepared. Have what you are going to tell them ready. 

* You can lie that "these two players are not the Demon" to exonerate your fellow evil players. 

* The Seer can sometimes get wildly confusing information. The {{Good|Recluse}} may register as the Demon. The Demon itself can change players to someone who was previously a Minion, even if no {{Evil|Successor}} is in play. The 'Red Herring' can register as the Demon, making a good player look suspicious. Worry less about the exact content of your information, and the number of 'yes' results you claim to have received, and more about acting with conviction. 

* Having noted the above point, your bluff is still increased dramatically if the information you reveal to the group is consistent. If you tell players over and over again that certain players are not the Demon, they will tend to keep those players alive. If you tell players over and over again that certain players are possibly the Demon, they will tend to kill them, but may come for you next when the game doesn't end afterwards.

*If a good player is being particularly troublesome, focusing all your attention on them by claiming that they are registering as the Demon to you can quickly get them killed. You can always claim that they must have been your 'Red Herring' after the fact. This strategy can be particularly devastating if you need to get a player executed, but can not do so by normal means. In particular, you can get a {{Good|Saint}} executed or a {{Good|Mayor}} or {{Good|Soldier}} killed, or at least convince the good team not to trust them. Even sowing distrust about an {{Good|Gravedigger}}, {{Good|Empath}}, or similar character can be enough to make their information questionable.

* Remember that Seers may choose dead players, and may even choose themselves. Telling the group that a dead player is the Demon implies that all their information was dubious, and also (in single Minion games) that only one remaining player is evil. This misinformation, whilst seemingly innocent, can sway the game in your direction due to the extra mistrust among the living players. It is particularly effective if you make a {{Good|Ravenkeeper}} appear to be the Demon that killed themselves at night time.

* If you are the {{Evil|Alpha Wolf}}, you can come out publicly as the Seer and strategically die at night to make your information sound more legitimate.

* If you are the {{Evil|Successor}}, you can publicly accuse your actual Demon and lead the charge for their execution. This gives characters like the {{Good|Gravedigger}} a positive read of 'Demon', solidifying your bluff.

* If you know of a {{Good|Recluse}}, claim to have gotten a 'yes' on them. If you are or have spoken to a {{Evil|Spy}}, you can claim to have a 'yes' on the {{Good|Recluse}} before they can claim their character, which makes your 'yes' look even more believable.
