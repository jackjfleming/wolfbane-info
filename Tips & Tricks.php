== Bluffing as the Hunter ==

When bluffing as the Hunter, there are a few things you should keep in mind:

* You would never wake in the night as the Hunter. Instead, you should publicly announce that you want to claim to be the Hunter, and the Storyteller will take care of the rest of your bluff for you. There is nothing tricky about this character as a bluff.

* Nobody will ever die as a result of you "using your Hunter ability". With this in mind, there are a few ways you can choose to handle it:
** Deliberately slaying a Demon or another evil player to make them look like they are not the Demon, which can be useful for setting up bluffs. This is an especially good tactic when combined with the {{Evil|Successor}}, since the real Demon can die and pass their ability to a player who has already been exonerated.
** Slaying a good player to confirm them as not the Demon; this will enable you to 'trust' them, and will enable them to want to team up with you because of it.
** Alternatively, you can deliberately slay a good player, and then start to appear evil (e.g. {{Good|Empath}}, setting yourself up to be read by an {{Good|Gravedigger}}, etc.) Since you 'protected' a player from suspicion with your ability, they will appear evil as well.

* Solicit the opinion of the group on who you should pick. Good players will be keen to help you make the right choice, and working with them will make you look good, also potentially coaxing information characters out from hiding who want to guide you to the right choice.

* The Hunter is a good "backup bluff". If you claim to be a character that is already in play, or you are otherwise under suspicion by the group, claiming that you were lying about who you were so that you could survive an extra day or two as the Hunter is believable - after all... that is what a real Hunter might very well do.

* Bluffing as a Hunter also works for good players! A Hunter with no ability is not a high priority target for the Demon, enabling you to survive and continue secretly gathering information.
