* Do whatever you can to convince the rest of the group that the player whose life you saved is good. This may be an uphill struggle, depending on the number of other Travellers in play. Since the group will know for a fact that you are both the same alignment, they may be able to use this information to prove or disprove other player's alignments. Pay attention, and focus on discrediting key players. For example, if the {{Evil|Alpha Wolf}} just got executed but saved by you, and the {{Good|Empath}} is sitting next to you and knows that one of their neighbours is evil, you will need to completely discredit either the {{Good|Empath}}, or their other living neighbour... otherwise it's curtains for team evil.

