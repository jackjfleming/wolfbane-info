== Summary ==
"You start knowing that 1 of 2 players is a particular Townsfolk."

The Housekeeper learns that a specific Townsfolk is in play, but not who is playing them.
* During the first night, the Housekeeper is woken, shown two players, and learns the character of one of them.
* They learn this only once and then learn nothing more.
		</div>

		<div class="small-12 large-6 columns" style="padding-right: 0;">
== How to Run ==
While preparing the first night, put the Housekeeper's '''TOWNSFOLK''' reminder token by any Townsfolk character token, and put the Housekeeper's '''WRONG''' reminder token by any other character token.

During the first night, wake the Housekeeper and point to the players marked '''TOWNSFOLK''' and '''WRONG'''. Show the character token marked '''TOWNSFOLK''' to the Housekeeper. Put the Housekeeper to sleep. Remove the Housekeeper's reminder tokens when convenient.
