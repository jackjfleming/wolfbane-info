== Bluffing as the Saint ==

When bluffing as the Saint, there are a few things you should keep in mind:

* You would never wake, learn anything, or act during the day, except on your execution, at which point the game is already over.

* Because of the Saint's lose condition, it is easily one of the best evil bluffs in the game. It is such a risk to execute you that the good team will usually err on the side of caution and leave you alone. This does not mean that it is bulletproof; on the final day, you're at the same risk as everyone else (since if they don't execute the Demon, they lose anyway). You'll also come under a reasonable amount of scrutiny as the game progresses.

* Saint is often an excellent bluff for the {{Evil|Poisoner}} in particular. Even if the good team truly believe you to be evil, perhaps with information from all three of the {{Good|Investigator}}, {{Good|Empath}}, and {{Good|Ravenkeeper}}, the good team may still be too scared to execute you before the final day, by when you've already done all the damage you need to. You can then either get executed and win for the evil team, or if you think the good team know you're not the Demon, have the {{Evil|Alpha Wolf}} attack themself in the night and make you the new Demon.

* If you are the {{Evil|Alpha Wolf}}, you know that your execution causes your team to lose if there's no {{Evil|Successor}} in the game. Because of that, if you're slated to be executed and try desperately to save yourself, the good team may correctly interpret that as a Demon trying to save themselves, at least long enough to kill themself and pass on the responsibilities to a Minion. That is severely limited by the Saint bluff, however - they may read that you want to avoid execution to stop your team from losing, but not know which team that is.

* Bluffing as the Saint in a game with no {{Evil|Baron}} can be tricky since you will likely be conflicting with other Outsiders. Fortunately, your lose condition makes you a larger threat to execute over a {{Good|Butler}} or {{Good|Recluse}}; use this to your advantage to leverage the execution of a good player.

* You may be detected as evil at some point during the game. A great counter-argument is that because you are the Saint, you are being targeted by the Storyteller. For example, you could say you are the {{Good|Seer}}'s false positive, or that an {{Good|Empath}} getting an evil read on you is actually {{Good|Village Idiot}}. 

