== Fighting the Imp ==

* In Trouble Brewing, fighting the Imp is the same as playing the game for the good team, so be sure to have a look at [[Player Strategy]]. Killing the Demon wins the game for good. If you have a lead on a possible Demon and another lead on a possible Minion, it's usually best to try prioritize executing the Demon suspects first!

* Finding out who the Imp ''isn't'' is almost as important as finding out who the Imp ''is''. Confirming other players as good allows you to build trust, put together information, and build a list of suspects... but it also means that you narrow down the field for who is not the Demon. On the final day, there are usually three players left alive, one of them being the Demon - if aren't certain of the Demon but you can say for sure that one of those people is good, then that gives you 50/50 odds to execute the right player.
** Characters like the {{Good|Ravenkeeper}} or {{Good|Hunter}} are especially good at pinning down people as not Demons - while it's exciting to find an evil player with these one shot abilities, finding a good player is just as powerful.

* If someone unexpectedly dies at an odd time, or after coming under a lot of suspicion, you may have just seen the Imp kill themselves and pass the Demonhood on to one of their Minions. This is fantastic information, because knowing this person is evil allows you to look at everything they have done to try and uncover the other members of the evil team. This is where information from characters like the {{Good|Chef}}, {{Good|Empath}} and {{Good|Investigator}} become particularly valuable, since you can use this player as a point of reference for pinning down the other evil characters. If those characters are not in play, you still have a lot of options: Who have they been talking to and built trust with? If they are bluffing as an information gathering character, who has their information confirmed or protected - for example, they may have bluffed as the {{Good|Housekeeper}} to "confirm" the bluff of their Minion and then died at night once people trusted the Minion. 

* Beware the {{Evil|Successor}}! So long as there are five or more players alive, the Imp will be able to be executed and the evil team will continue. If you think you know who the Imp is, consider holding off on the execution until you have fewer than five players so that her ability cannot activate, thus stopping the Demon in their tracks right then and there.

* The Imp's ability to kill means that they can be extremely effective at removing characters that pose a threat to them, and they will do so with gusto. To protect your players getting ongoing information (like the {{Good|Gravedigger}}) or with powerful one-time abilities (like the {{Good|New in Town}}), try to trick the Imp into killing players who are not good choices. For example, a {{Good|Soldier}} or {{Good|Mayor}} could bluff as a juicy target, letting their abilities soak up the hit. Alternatively, characters like the {{Good|Saint}} (who would much prefer to die at night), {{Good|Recluse}}, or the {{Good|Housekeeper}} could try to attract the attention of the Demon - they will die, but better them than your {{Good|Seer}}!

* If you are playing in a game with six or fewer players, the rules for the Demon change slightly. They do not start with bluffs, and they do not know who their Minion is. One fun strategy is to try bluffing as the Minion to another player, and encouraging the Demon to confide in you. If they admit to being the Demon, you can out them to the town for a decisive victory.

* Unlike other Demons you may encounter, the Imp's special ability to pass on the Demonhood to their Minions puts them in the unique position of being able to get away with more complicated bluffing strategies, often involving more than one player. When looking for evidence of evil characters, keeping an eye on groups of characters confirming each other. For example:
** They could bluff as a {{Good|Housekeeper}}/{{Good|Librarian}} and confirm their Minion is a Townsfolk/Outsider. (Particularly potent if the Minion is claiming to be the {{Good|Saint}}!)
** They bluff as the {{Good|Empath}} claiming to have two good neighbour shortly before their untimely demise, passing to their neighbouring Minion.
** They can choose to die and then bluff as the {{Good|Ravenkeeper}}, confirming the bluff of their Minion now turned Demon.
** They can bluff as the {{Good|Hunter}}, "slaying" their Minion and then dying. (They can also do this trick with an actually good {{Good|Hunter}}, getting them to target the Minion and passing to them ''after'' they've been confirmed not the Demon).
** They can choose to deliberately kill nobody on a night to allow their Minion to bluff as a {{Good|Soldier}} or {{Good|Bodyguard}}. People are very likely to trust a {{Good|Soldier}} if there's been no kill, making them ideal for taking over the Demonhood later.

