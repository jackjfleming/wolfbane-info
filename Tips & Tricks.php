== Tips & Tricks ==

* If the Demon knows you are the Ravenkeeper, they are very unlikely to kill you. It is to your benefit to bluff as a character who is a constant threat to the evil team, such as the {{Good|Empath}}, {{Good|Seer}}, {{Good|Hunter}}, or {{Good|Gravedigger}}. Even bluffing as a good character that tends to be trusted by the good team (such as {{Good|Chef}} or {{Good|Housekeeper}}) can make you look like a trusted good player in the eyes of the Demon, and attract their attention at night.

